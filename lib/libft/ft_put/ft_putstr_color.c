#include "../include/libft.h"

void	ft_putstr_color(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	write(1, "\033[1;32m", 8);
	write(1, str, i);
	write(1, "\033[0m", 5);
}

#include "../include/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	f_n;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	f_n = (n % 10) + '0';
	write(fd, &f_n, 1);
}

#include "../include/libft.h"

void	ft_puterror(char *str)
{
	if (!str)
	{
		write(2, "\033[1;31mError\033[0m\n", 18);
	}
	else
	{
		write(2, "\033[1;31m", 8);
		write(2, str, ft_strlen(str));
		write(2, "\033[0m\n", 6);
	}
}

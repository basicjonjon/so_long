#include "../include/libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t					i;
	unsigned char			*d;
	const unsigned char		*s;

	i = 0;
	d = dest;
	s = src;
	if (d == 0 && s == 0)
		return (dest);
	if (d > s)
	{
		while (n > 0)
		{
			d[n - 1] = s[n - 1];
			n--;
		}
	}
	else
		while (i < n)
			d[i++] = *s++;
	return (dest);
}
